const { getDb } = require("./db");
const { publishDose, shouldSendNow } = require("./newsletter-send");

let timer = null;

async function tick() {
  const db = getDb();
  try {
    if (!shouldSendNow(db)) return;
    const result = await publishDose(db, { autoGenerated: 1 });
    if (result.ok) {
      console.log(
        `[newsletter] جرعة وعي تلقائية: «${result.dose.subject}» — بريد ${result.emailsOk} · إشعار ${result.push.sent}/${result.push.total}`
      );
    }
  } catch (err) {
    console.error("[newsletter]", err.message);
  } finally {
    db.close();
  }
}

function startNewsletterScheduler() {
  if (process.env.NEWSLETTER_AUTO === "0") {
    console.log("[newsletter] الإرسال التلقائي معطّل (NEWSLETTER_AUTO=0)");
    return;
  }
  const intervalMin = parseInt(process.env.NEWSLETTER_CHECK_MINUTES || "30", 10);
  const ms = Math.max(5, intervalMin) * 60 * 1000;
  if (timer) clearInterval(timer);
  setTimeout(() => tick(), 8000);
  timer = setInterval(() => tick(), ms);
  console.log(`[newsletter] مجدول جرعات الوعي — فحص كل ${intervalMin} دقيقة`);
}

module.exports = { startNewsletterScheduler, tick };
